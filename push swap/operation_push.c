#include "push_swap.h"

void	ft_pa(t_stack stack)
{
	int	tmp;
	int	i;

	if (stack.b == NULL)
		return;
	while (stack.a[0] != stack.b[0])
	{
		i = 0;
		while (i < stack.size - 1)
		{
			tmp = stack.a[i];
			stack.a[i] = stack.a[i + 1];
			stack.a[i + 1] = tmp;
			i++;
		}
	}
}

void	ft_pb(t_stack stack)
{
	int	tmp;
	int	i;

	while (stack.a[0] != stack.b[0])
	{
		i = 0;
		while (i < stack.size - 1)
		{
			tmp = stack.b[i];
			stack.b[i] = stack.b[i + 1];
			stack.b[i + 1] = tmp;
			i++;
		}
	}
}

int	*ft_bzero(t_stack stack)
{
	int	i;
	i = 0;

	stack.b = (int *)malloc(sizeof(int) * stack.size);
	while (i < stack.size)
	{
		stack.b[i] = '\0';
		i++;
	}
	return (stack.b);
}