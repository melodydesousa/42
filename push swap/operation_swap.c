#include "push_swap.h"

void	ft_sa(t_stack stack)
{
	int	i;
	int	tmp;

	i = 0;
	while (i < 1)
	{
		tmp = stack.a[i];
		stack.a[i] = stack.a[i + 1];
		stack.a[i + 1] = tmp;
		i++;
	}
}

void	ft_sb(t_stack stack)
{
	int	i;
	int	tmp;

	i = 0;
	while (i < 1)
	{
		tmp = stack.b[i];
		stack.b[i] = stack.b[i + 1];
		stack.b[i + 1] = tmp;
		i++;
	}
}

void ft_ss(t_stack stack)
{
	ft_sa(stack);
	ft_sb(stack);
}
